//----------------------  03. What is the smart contract blockchain -------------------------------

// Smart contract blockchains, such as Ethereum, provide developers with a decentralized platform for executing code. The code of a smart contract is stored on the blockchain and is executed automatically when certain conditions are met.

//Additionally, smart contract blockchains are transparent, as every transaction on the blockchain is publicly visible and can be verified by anyone. This makes it possible for users to trust the system without having to rely on a central authority.

//Overall, smart contract blockchains provide a powerful platform for developers to create decentralized applications that can be used in a variety of industries, including finance, supply chain management, and healthcare, among others.

